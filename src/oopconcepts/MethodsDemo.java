package oopconcepts;

public class MethodsDemo {
	
	/*
	 * Every action must be written inside a method
	 * Only variable declaration can be outside a method
	 * Every java class needs a main method. If there is no main method, 
	 * the class cannot be run on its own. It must be called in other classes.
	 */

	public static void main(String[] args) {
		String a = "Hey, there !!!";
		System.out.println(a);
	}
	
	public static void methodNum() {
		String a = "Hey, there !!!";
		System.out.println(a);
	}
}